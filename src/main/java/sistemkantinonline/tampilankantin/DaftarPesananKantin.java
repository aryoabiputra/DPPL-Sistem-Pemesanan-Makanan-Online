    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
    
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih pesanan yang akan diproses.", 
                                          "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String currentStatus = tableModel.getValueAt(selectedRow, 7).toString();

        if (!currentStatus.equals("Baru")) {
            JOptionPane.showMessageDialog(this, "Pesanan sudah diproses atau selesai.", 
                                          "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        tableModel.setValueAt("Diproses", selectedRow, 7);
        JOptionPane.showMessageDialog(this, "Pesanan dipindahkan ke status 'Diproses'.");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
    
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih pesanan yang sudah selesai.", 
                                          "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String currentStatus = tableModel.getValueAt(selectedRow, 7).toString();

        // Validasi: Pesanan harus Diproses terlebih dahulu
        if (currentStatus.equals("Baru")) {
            JOptionPane.showMessageDialog(this, "Pesanan harus diproses terlebih dahulu.", 
                                          "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        tableModel.setValueAt("Siap Diambil", selectedRow, 7);
        JOptionPane.showMessageDialog(this, "Status pesanan diubah menjadi 'Siap Diambil'.");
    }//GEN-LAST:event_jButton2ActionPerformed


