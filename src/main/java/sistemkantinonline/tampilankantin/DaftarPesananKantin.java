    public void autoResizeColumns() {
        if (jTable1 == null || jTable1.getRowCount() == 0) return;
        
        TableColumnModel columnModel = jTable1.getColumnModel();
        
        for (int column = 0; column < jTable1.getColumnCount(); column++) {
            int width = 50;
            
            TableCellRenderer headerRenderer = jTable1.getTableHeader().getDefaultRenderer();
            Component headerComp = headerRenderer.getTableCellRendererComponent(
                    jTable1, columnModel.getColumn(column).getHeaderValue(), false, false, 0, 0);
            width = Math.max(width, headerComp.getPreferredSize().width);
            
            for (int row = 0; row < jTable1.getRowCount(); row++) {
                TableCellRenderer renderer = jTable1.getCellRenderer(row, column);
                Component comp = jTable1.prepareRenderer(renderer, row, column);
                width = Math.max(comp.getPreferredSize().width + jTable1.getIntercellSpacing().width, width); 
            }
            columnModel.getColumn(column).setPreferredWidth(width + 10);
        }
    }
    
